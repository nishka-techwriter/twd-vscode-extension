import * as vscode from 'vscode';

/**
 * Предоставляет статические методы для вставки текстовых сниппетов
 * в активный редактор VS Code.
 *
 * Все сниппеты вставляются с использованием {@link vscode.SnippetString}
 * и могут содержать плейсхолдеры для последовательного редактирования
 * после вставки.
 *
 * Класс не хранит состояния и предназначен для использования
 * в командах расширения (commands).
 */
export class SnippetInserter {

    /**
     * Вставляет базовый демонстрационный сниппет в активный редактор.
     *
     * Используется как пример работы с {@link vscode.SnippetString}
     * и может быть заменён или расширен пользовательским шаблоном.
     *
     * Если активный текстовый редактор отсутствует,
     * вставка сниппета не выполняется.
     */
    static insertMySnippet(): void {
        const snippet = new vscode.SnippetString('<!-- Здесь будет ваш сниппет -->');
        vscode.window.activeTextEditor?.insertSnippet(snippet);
    }

    /**
     * Вставляет шаблон таблицы в формате Markdown.
     *
     * Сниппет содержит выравнивание колонок и поддерживает
     * последовательную навигацию по плейсхолдерам.
     *
     * Плейсхолдеры:
     * - ${1}–${3} — заголовки таблицы
     * - ${4}–${9} — значения ячеек первой и второй строки
     *
     * Если активный текстовый редактор отсутствует,
     * вставка сниппета не выполняется.
     */
    static insertMarkdownTable(): void {
        const snippet = new vscode.SnippetString(
            '| ${1:Заголовок 1} | ${2:Заголовок 2} | ${3:Заголовок 3} |\n' +
            '| :--- | :--- | :--- |\n' +
            '| ${4:Ячейка 1} | ${5:Ячейка 2} | ${6:Ячейка 3} |\n' +
            '| ${7:Ячейка 4} | ${8:Ячейка 5} | ${9:Ячейка 6} |'
        );
        vscode.window.activeTextEditor?.insertSnippet(snippet);
    }

    /**
     * Вставляет HTML-шаблон раскрывающегося блока (`<details>`)
     * для использования в Markdown-документах.
     *
     * Поддерживает плейсхолдеры для заголовка и содержимого блока.
     *
     * Плейсхолдеры:
     * - ${1} — текст заголовка (`<summary>`)
     * - ${2} — содержимое раскрываемого блока
     *
     * Если активный текстовый редактор отсутствует,
     * вставка сниппета не выполняется.
     */
    static insertMarkdownDetails(): void {
        const snippet = new vscode.SnippetString(
            '<details>\n' +
            '  <summary>${1:Нажмите чтобы раскрыть}</summary>\n\n' +
            '  ${2:Здесь находится скрытое содержимое...}\n' +
            '</details>'
        );
        vscode.window.activeTextEditor?.insertSnippet(snippet);
    }
}